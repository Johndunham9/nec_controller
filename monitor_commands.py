# Monitor commands


# Default Monitor ID is 1/A. This can be changed
MONITOR_ID = 0x41
CARRIAGE_RETURN = 0x0D

# CONTROL CHARACTERS
SOH, STX, ETX = 0x01, 0x02, 0x03


# COMMANDS EXCLUDE BCC AND CR bytes.
# Get Monitor status command
status_header = [0x30, MONITOR_ID, 0x30, 0x41, 0x30, 0x36]
status_message_block = [STX, 0x30, 0x31, 0x44, 0x36, ETX]

# Monitor ON Command
on_header = [0x30, MONITOR_ID, 0x30, 0x41, 0x30, 0x43]
on_message_block = [STX, 0x43, 0x32, 0x30, 0x33, 0x44, 0x36, 0x30, 0x30, 0x30, 0x31, ETX]

# Monitor OFF Command
off_header = [0x30, MONITOR_ID, 0x30, 0x41, 0x30, 0x43]
off_message_block = [STX, 0x43, 0x32, 0x30, 0x33, 0x44, 0x36, 0x30, 0x30, 0x30, 0x34, ETX]

# Monitor Volume Command (This command is further built by controller)
vol_header = [0x30, MONITOR_ID, 0x30, 0x45, 0x30, 0x41]
vol_message_block = [STX, 0x30, 0x30, 0x36, 0x32] # +ETX

# For The Monitor Status Read
power_status_read_header = [0x30, MONITOR_ID, 0x30, 0x41, 0x30, 0x36]
power_status_read_message_block = [STX, 0x30, 0x31, 0x44, 0x36, ETX]

# For Simulated Monitor Power Status Reply
on_status_reply_header = [0x30, 0x30, MONITOR_ID, 0x42, 0x31, 0x32]
on_status_reply_block = [STX, 0x30, 0x32, 0x30, 0x30, 0x44, 0x36, 0x30, 0x30, 0x30, 0x30, 0x30, 0x31, ETX]

off_status_reply_header = [0x30, 0x30, MONITOR_ID, 0x42, 0x31, 0x32]
off_status_reply_block = [STX, 0x30, 0x32, 0x30, 0x30, 0x44, 0x36, 0x30, 0x30, 0x30, 0x30, 0x30, 0x34, ETX]

